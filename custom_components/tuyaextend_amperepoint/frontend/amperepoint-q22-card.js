  rawRows() {
    const raw = this.config.entities.rawDp;
    const snapshot = this.attr(raw, "raw_dp", null);
    const metadata = this.attr(raw, "dp_metadata", {}) || {};
    const knownDpIds = {
      forward_energy_total: 1,
      work_state: 3,
      charge_cur_set: 4,
      phase_a: 6,
      phase_b: 7,
      phase_c: 8,
      power_total: 9,
      fault: 10,
      connection_state: 13,
      work_mode: 14,
      energy_charge: 17,
      switch: 18,
      local_timer: 19,
      system_version: 23,
      temp_current: 24,
      charge_energy_once: 25,
      mode_set: 33,
    };

    if (
      snapshot &&
      typeof snapshot === "object" &&
      !Array.isArray(snapshot) &&
      Object.keys(snapshot).length > 0
    ) {
      return Object.entries(snapshot)
        .map(([code, value]) => {
          const definition = metadata[code] || {};
          return {
            code,
            value,
            definition,
            dp: definition.dp_id ?? knownDpIds[code] ?? "–",
          };
        })
        .sort((a, b) => {
          const aDp = typeof a.dp === "number" ? a.dp : Number.MAX_SAFE_INTEGER;
          const bDp = typeof b.dp === "number" ? b.dp : Number.MAX_SAFE_INTEGER;
          return aDp - bDp || a.code.localeCompare(b.code);
        })
        .map(({ code, value, definition, dp }) => {
          let decoded = value;
          if (typeof value === "number" && Number(definition.scale || 0) > 0) {
            decoded = value / 10 ** Number(definition.scale);
          }
          if (typeof value === "boolean") decoded = value ? "true" : "false";
          if (typeof decoded === "object") decoded = JSON.stringify(decoded);
          const unit = definition.unit ? ` ${this.escape(definition.unit)}` : "";
          const access = definition.writable ? " ↔" : "";
          return `
            <tr>
              <td>${this.escape(dp)}</td>
              <td><code>${this.escape(code)}</code>${access}</td>
              <td><code>${this.escape(typeof value === "object" ? JSON.stringify(value) : value)}</code></td>
              <td>${this.escape(decoded)}${unit}</td>
            </tr>
          `;
        })
        .join("");
    }

    const rowDefs = [
      ["1", "forward_energy_total", "raw_forward_energy_total", this.attr(raw, "forward_energy_total_kwh"), "kWh"],
      ["3", "work_state", "raw_work_state", this.human(this.state(this.config.entities.status)), ""],
      ["4", "charge_cur_set", "raw_charge_cur_set", this.attr(raw, "charge_current_a"), "A"],
      ["6", "phase_a", "raw_phase_a", this.phaseRawText(raw, "phase_a"), ""],
      ["7", "phase_b", "raw_phase_b", this.phaseRawText(raw, "phase_b"), ""],
      ["8", "phase_c", "raw_phase_c", this.phaseRawText(raw, "phase_c"), ""],
      ["9", "power_total", "raw_power_total", this.attr(raw, "power_total_kw"), "kW"],
      ["10", "fault", "raw_fault", this.human(this.state(this.config.entities.faults)), ""],
      ["13", "connection_state", "raw_connection_state", this.human(this.state(this.config.entities.cp)), ""],
      ["17", "energy_charge", "raw_energy_charge", this.attr(raw, "energy_charge_kwh"), "kWh"],
      ["18", "switch", "raw_switch", this.state(this.config.entities.switch), ""],
      ["24", "temp_current", "raw_temp_current", this.attr(raw, "temp_current_c"), "C"],
      ["25", "charge_energy_once", "raw_charge_energy_once", this.attr(raw, "charge_energy_once_kwh"), "kWh"],
    ];

    return rowDefs
      .filter(([, , rawKey, decoded]) => this.hasAttr(raw, rawKey) || !this.isMissingValue(decoded))
      .map(([dp, code, rawKey, decoded, unit]) => {
        const rawValue = this.attr(raw, rawKey, null);
        const decodedText = this.isMissingValue(decoded) ? "" : `${this.escape(decoded)}${unit ? ` ${unit}` : ""}`;
        return `
          <tr>
            <td>${dp}</td>
            <td><code>${code}</code></td>
            <td>${rawValue === null ? "" : `<code>${this.escape(rawValue)}</code>`}</td>
            <td>${decodedText}</td>
          </tr>
        `;
      })
      .join("");
  }

  phaseRawText(raw, prefix) {
    const voltage = this.attr(raw, `${prefix}_voltage_v`, null);
    const current = this.attr(raw, `${prefix}_current_a`, null);
    const power = this.attr(raw, `${prefix}_power_kw`, null);
    if (this.isMissingValue(voltage) && this.isMissingValue(current) && this.isMissingValue(power)) return null;
    return `${voltage ?? "-"} V / ${current ?? "-"} A / ${power ?? "-"} kW`;
  }

