  phase(phase) {
    const voltage = this.hasEntity(phase.voltage) ? this.num(phase.voltage) : null;
    const current = this.hasEntity(phase.current) ? this.num(phase.current) : null;
    const power = this.hasEntity(phase.power) ? this.num(phase.power) : null;
    const active = (current || 0) > 0.2 || (power || 0) > 0.05;
    const width = power === null ? 2 : Math.max(2, Math.min(100, (power / this.config.phaseMaxPowerKw) * 100));
    const cells = [
      voltage !== null ? `<div><span>${this.t("voltage")}</span><b>${this.fmt(voltage, "V", 0)}</b></div>` : "",
      current !== null ? `<div><span>${this.t("current")}</span><b>${this.fmt(current, "A", 1)}</b></div>` : "",
    ].join("");
    return `
      <div class="phase ${active ? "active" : ""}">
        <div class="phase-head">
          <div>
            <strong>${phase.label}</strong>
            <span>${active ? this.t("active") : this.t("readyPhase")}</span>
          </div>
          ${power !== null ? `<b>${this.fmt(power, "kW", 2)}</b>` : ""}
        </div>
        ${power !== null ? `<div class="bar"><i style="width:${width}%"></i></div>` : ""}
        ${cells ? `<div class="phase-grid">${cells}</div>` : ""}
      </div>
    `;
  }

